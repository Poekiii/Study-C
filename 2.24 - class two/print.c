//输出什么？
#include <stdio.h>
int main()
{
    char a= -1;  //char 默认有符号
    signed char b=-1;
    //原1000000000000001
    //反1111111111111110
    //补1111111111111111
    //char只存8个bit 即 1111 1111
    //%d有符号整型输出 因为原char是有符号，则加长成 1111111111111 1111 1111
    //再 反—>原 即100000000001 = -1  

    unsigned char c=-1;
    //同上 最终取c的补为 1111 1111   ps:机器储存都是补码
    //但是由于uchar是无符号储存 则%d输出时候加长补0 即000000000000 1111 1111  =255

    printf("a=%d,b=%d,c=%d",a,b,c);
    // -1 -1 255
    return 0;
}


